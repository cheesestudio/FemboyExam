// -------------------------------------------
// 特定组合检测
// -------------------------------------------
function detectPatterns(scores) {
    const patterns = [];
    const variance = Math.max(...scores) - Math.min(...scores);

    if (variance <= 1) patterns.push('very_consistent');
    if (variance >= 3) patterns.push('highly_balanced');

    const extremes = scores.filter(s => s === 0 || s === 3);
    if (extremes.length >= 4) patterns.push('very_distinct');
    if (extremes.length === 0) patterns.push('well_balanced');

    if (scores[0] >= 2 && scores[1] <= 1) patterns.push('creative_spontaneous');
    if (scores[0] <= 1 && scores[1] >= 2) patterns.push('practical_organized');
    if (scores[2] >= 2 && scores[4] >= 2) patterns.push('charismatic_stable');
    if (scores[2] <= 1 && scores[4] <= 1) patterns.push('deep_thoughtful');
    if (scores[3] >= 2 && scores[5] <= 1) patterns.push('harmonious_thoughtful');
    if (scores[3] <= 1 && scores[5] >= 2) patterns.push('direct_decisive');
    if (scores[1] >= 2 && scores[5] >= 2) patterns.push('driven_executor');
    if (scores[0] >= 2 && scores[4] <= 1) patterns.push('creative_sensitive');

    return patterns;
}

// -------------------------------------------
// 哈希函数 - 确保相同输入产生相同输出
// -------------------------------------------
function hashScores(scores, salt = 0) {
    let hash = 17;
    for (let i = 0; i < scores.length; i++) {
        hash = hash * 31 + scores[i];
        hash = hash * 31 + i;
    }
    return (hash + salt) >>> 0;
}

function seededRandom(hash, offset = 0) {
    const x = Math.sin(hash + offset) * 10000;
    return x - Math.floor(x);
}

// -------------------------------------------
// 主生成函数
// -------------------------------------------
function generateReport(scores) {
    if (!Array.isArray(scores) || scores.length !== 6) {
        throw new Error('Expected array of 6 dimension scores (one per personality dimension)');
    }
    scores.forEach(s => {
        if (!Number.isInteger(s) || s < 0 || s > 3) {
            throw new Error('Each score must be an integer between 0 and 3 inclusive');
        }
    });

    const hash = hashScores(scores);
    const detectedPatterns = detectPatterns(scores);

    const opening = OPENINGS[Math.floor(seededRandom(hash, 1) * OPENINGS.length)];

    const paragraphs = [];
    const usedVariants = new Set();

    // 生成每个维度的基础描述和深度分析
    REPORT_DIMENSIONS.forEach((dim, index) => {
        const level = scores[index];
        const templates = BASE_TEMPLATES[dim.id][level];

        let variantIndex;
        do {
            variantIndex = Math.floor(seededRandom(hash, index * 100 + 7) * templates.length);
        } while (usedVariants.has(`${dim.id}-${variantIndex}`) && usedVariants.size < templates.length);
        usedVariants.add(`${dim.id}-${variantIndex}`);

        let text = templates[variantIndex];

        // 随机添加修饰词
        if (seededRandom(hash, index * 100 + 23) > 0.5) {
            const modType = ['intensity', 'qualification', 'perspective'][Math.floor(seededRandom(hash, index * 100 + 41) * 3)];
            const modifier = MODIFIERS[modType][Math.floor(seededRandom(hash, index * 100 + 53) * MODIFIERS[modType].length)];

            if (modType === 'perspective') {
                text = `${modifier} ${text.charAt(0).toLowerCase() + text.slice(1)}`;
            } else {
                const parts = text.split(' ');
                parts.splice(1, 0, modifier);
                text = parts.join(' ');
            }
        }

        paragraphs.push(text);

        // 添加该维度的深度分析
        if (EXTENDED_ANALYSIS[dim.id] && EXTENDED_ANALYSIS[dim.id][level]) {
            const analyses = EXTENDED_ANALYSIS[dim.id][level];
            // 根据哈希值决定是否添加深度分析
            // 原先是 analysisCount 可以是 1 或 2，现在改为只有 3/5 概率添加 1 条
            const shouldAdd = seededRandom(hash, index * 200 + 100) > 0.4;
            if (shouldAdd && analyses.length > 0) {
                paragraphs.push(analyses[0]);
            }
        }
    });

    // 添加跨维度的综合分析（减少到最多1条，控制总体体量）
    const crossDimensionalInsights = generateCrossDimensionalInsights(scores, hash);
    if (crossDimensionalInsights.length > 0) {
        paragraphs.push(crossDimensionalInsights[0]);
    }

    // 洗牌段落顺序（但保持开头和结尾固定）
    // 只洗牌中间段落
    const middleStart = 0;
    const middleEnd = paragraphs.length;
    for (let i = middleEnd - 1; i > middleStart; i--) {
        const j = Math.floor(seededRandom(hash, i * 79) * (i + 1));
        [paragraphs[i], paragraphs[j]] = [paragraphs[j], paragraphs[i]];
    }

    // 为每个段落（从第二个开始）添加连接词
    for (let i = 1; i < paragraphs.length; i++) {
        const connector = CONNECTORS[Math.floor(seededRandom(hash, i * 127 + 19) * CONNECTORS.length)];
        paragraphs[i] = `${connector} ${paragraphs[i].charAt(0).toLowerCase() + paragraphs[i].slice(1)}`;
    }

    const closing = CLOSINGS[Math.floor(seededRandom(hash, 997) * CLOSINGS.length)];

    // 处理段落格式：首行缩进2个全角空格，添加装饰符号
    const processedParagraphs = paragraphs.map(p => {
        const trimmed = p.trim();
        // 选择装饰符号
        const symbols = ['✨', '🌟', '💫', '🎯', '🌈', '💡', '🎨', '📖', '🔍', '🌸'];
        const symbolIndex = Math.abs(trimmed.charCodeAt(0) + trimmed.length) % symbols.length;
        const symbol = symbols[symbolIndex];
        // 首行缩进两个全角空格
        const indented = trimmed.replace(/^(\S)/, '　　$1');
        return `${symbol} ${indented}`;
    });

    const finalReport = [
        '　　' + opening,
        '',
        ...processedParagraphs,
        '',
        '　　' + closing
    ].join('\n');

    return {
        report: finalReport,
        scores: scores,
        detectedPatterns: detectedPatterns,
        statistics: {
            baseVariants: 10 ** 6,
            modifierCombinations: 2 ** 6,
            connectorVariants: 13 ** 5,
            openingVariants: 7,
            closingVariants: 8,
            paragraphOrderings: 720,
            totalUniquePossible: 7.18e17
        }
    };
}

// -------------------------------------------
// 跨维度综合分析生成
// -------------------------------------------
function generateCrossDimensionalInsights(scores, hash) {
    const insights = [];
    const dimNames = ['脑洞大小', '靠谱程度', '社牛等级', '老好人指数', '情绪稳定度', '果断程度'];

    // 寻找最高和最低的维度
    let maxIdx = 0, minIdx = 0;
    for (let i = 1; i < scores.length; i++) {
        if (scores[i] > scores[maxIdx]) maxIdx = i;
        if (scores[i] < scores[minIdx]) minIdx = i;
    }

    if (scores[maxIdx] >= 2) {
        const highInsight = `最突出的是你的${dimNames[maxIdx]}，这在你的整体性格中扮演了主导角色，很大程度上塑造了你待人接物的方式。`;
        insights.push(highInsight);
    }

    if (scores[minIdx] <= 1) {
        const lowInsight = `相对来说，你的${dimNames[minIdx]}并不突出，但这恰恰为其他特质的发展留出了空间，形成了独特的平衡。`;
        insights.push(lowInsight);
    }

    // 检查有趣的内在矛盾
    if (scores[0] >= 2 && scores[1] <= 1) {
        const contradiction = `有趣的是，你既有天马行空的创造力，又保持着务实的行动力，这种组合让你既能想得出点子又能落地实现。`;
        insights.push(contradiction);
    }

    if (scores[2] >= 2 && scores[3] <= 1) {
        const contradiction = `你在社交中既能够游刃有余地表达，又保持着一定的独立性，不会因为迎合他人而失去自我。`;
        insights.push(contradiction);
    }

    if (scores[4] >= 2 && scores[5] <= 1) {
        const contradiction = `情绪上的稳定加上决策时的谨慎，让你在面对重要选择时能够既冷静又周全，很少冲动行事。`;
        insights.push(contradiction);
    }

    // 如果洞察还不够，添加一个通用综合分析
    if (insights.length < 2) {
        const avgScore = scores.reduce((a, b) => a + b, 0) / scores.length;
        if (avgScore >= 2) {
            insights.push(`整体来看，你展现出了相当成熟和平衡的性格特质，各个维度之间配合得宜，这让你在不同情境下都能找到合适的应对方式。`);
        } else if (avgScore <= 1) {
            insights.push(`你的性格中有一种特别的真实和直接，不刻意掩饰自己的偏好，这种坦率在人际交往中反而建立起了独特的信任感。`);
        } else {
            insights.push(`这些特质组合在一起，形成了一个既独特又协调的整体，你能够在保持自我的同时灵活适应不同的环境要求。`);
        }
    }

    return insights;
}

